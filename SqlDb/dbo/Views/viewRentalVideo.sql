CREATE VIEW dbo.viewRentalVideo
AS
SELECT        dbo.tbl_rentalVideo.rentalVideoId, dbo.tbl_customer.name, dbo.tbl_customer.address, dbo.tbl_customer.mobile_no, dbo.tbl_video.video_title, dbo.tbl_video.release_date, dbo.tbl_video.rental_cost, 
                         dbo.tbl_rentalVideo.rentalDate, dbo.tbl_rentalVideo.returnDate, dbo.tbl_customer.customer_id, dbo.tbl_video.video_id
FROM            dbo.tbl_customer INNER JOIN
                         dbo.tbl_rentalVideo ON dbo.tbl_customer.customer_id = dbo.tbl_rentalVideo.customerId INNER JOIN
                         dbo.tbl_video ON dbo.tbl_rentalVideo.videoId = dbo.tbl_video.video_id

